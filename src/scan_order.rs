const fn invert<const N: usize>(scan: [u16; N]) -> [u16; N] {
  let mut iscan = scan;
  let mut i = 0;
  while i < N {
    iscan[scan[i] as usize] = i as u16;
    i += 1;
  }
  iscan
}

